public class Labor
{
    private String gebaude;
    private int stock;
    private boolean beamer;
    private int anzPlatze;
    
    public String getGebaude()
    {
        return gebaude;
    }
    
    public int getStock()
    {
        return stock;
    }
    
    public boolean getBeamer()
    {
        return beamer;
    }
    
    public int getAnzPlatze()
    {
        return anzPlatze;
    }
    
    public void setGebaude(String neuGebaude)
    {
        gebaude = neuGebaude;
    }
    
    public void setStock(int neuStock)
    {
        stock = neuStock;
    }
    
    public void setBeamer(boolean neuBeamer)
    {
        beamer = neuBeamer;
    }
    
    public void setAnzPlatze(int neuAnzPlatze)
    {
        anzPlatze = neuAnzPlatze;
    }
}
